int		ft_str_is_numeric(char *str)
{
	int i;
	int check;

	i = 0;
	check = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= '0' && str[i] <= '9')
			check++;
		i++;
	}
	if (i == check)
		return (1);
	else
		return (0);
}
