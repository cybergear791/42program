#include <stdio.h>

void ft_print_comb2(void){
int i = 0;
while(i<99){

	int j = i+1;
	while(j < 99){
		printf("%d %d, ",i, j );
		j++;
	}
	i++;
}
}

int main() {
ft_print_comb2();
}
